#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	c %= 256;
	while (*s != c)
	{
		if (*s == '\0')
		{
			if (c == 0)
				return ((char *)s);
			else
				return (NULL);
		}
		s++;
	}
	return ((char *)s);
}
